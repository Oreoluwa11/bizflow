import * as React from "react";
import { cn } from "@/lib/utils";

interface ButtonProps extends React.ButtonHTMLAttributes<HTMLButtonElement> {
  variant?: "default" | "outline" | "ghost" | "destructive";
  size?: "default" | "sm" | "lg" | "icon";
}

const Button = React.forwardRef<HTMLButtonElement, ButtonProps>(
  ({ className, variant = "default", size = "default", ...props }, ref) => {
    return (
      <button
        ref={ref}
        className={cn(
          "inline-flex items-center justify-center rounded-lg font-medium transition-colors focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-indigo-500 disabled:pointer-events-none disabled:opacity-50 active:scale-95 transition-transform",
          {
            "bg-indigo-600 text-white hover:bg-indigo-500 shadow-lg shadow-indigo-500/20 shadow-indigo-500/0":
              variant === "default",
            "border border-zinc-800 bg-transparent hover:bg-zinc-800 text-zinc-100":
              variant === "outline",
            "hover:bg-zinc-800 text-zinc-100": variant === "ghost",
            "bg-rose-600 text-white hover:bg-rose-500":
              variant === "destructive",
            "h-10 px-4 py-2": size === "default",
            "h-8 px-3 text-xs": size === "sm",
            "h-12 px-8": size === "lg",
            "h-10 w-10": size === "icon",
          },
          className
        )}
        {...props}
      />
    );
  }
);
Button.displayName = "Button";

export { Button };
